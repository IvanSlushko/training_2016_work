package com.ivanslushko.training.datamodel;

public class FlightFromCity {
	private City city;
	private Flight flight;

	public Object Date;
	public Object Too;
	public Object FrCity;
	public Object Plane;

	public City getCity() {
		return city;
	}

	public void setCity(City city) {
		this.city = city;
	}

	public Flight getFlight() {
		return flight;
	}

	public void setFlight(Flight flight) {
		this.flight = flight;
	}

	public Object getDate() {
		return Date;
	}

	public void setDate(Object date) {
		Date = date;
	}

	public Object getToo() {
		return Too;
	}

	public void setToo(Object too) {
		Too = too;
	}

	public Object getFrCity() {
		return FrCity;
	}

	public void setFrCity(Object frCity) {
		FrCity = frCity;
	}

	public Object getPlane() {
		return Plane;
	}

	public void setPlane(Object plane) {
		Plane = plane;
	}

}
